"use client";

import * as React from "react";
import { Check, ChevronsUpDown } from "lucide-react";

import { cn } from "@/lib/utils";
import { Button } from "@/components/ui/button";
import {
  Command,
  CommandEmpty,
  CommandGroup,
  CommandInput,
  CommandItem,
  CommandList,
} from "@/components/ui/command";
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from "@/components/ui/popover";

const jurisdictions = [
  { value: "us", label: "United States" },
  { value: "uk", label: "United Kingdom" },
  { value: "ca", label: "Canada" },
  { value: "au", label: "Australia" },
  { value: "in", label: "India" },
  { value: "np", label: "Nepal" },
  { value: "cn", label: "China" },
  { value: "eu", label: "European Union" },
];

interface JurisdictionSelectProps {
  value: string;
  onChange: (value: string) => void;
}

export function JurisdictionSelect({ value, onChange }: JurisdictionSelectProps) {
  const [open, setOpen] = React.useState(false);

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          variant="outline"
          role="combobox"
          aria-expanded={open}
          className="w-[180px] justify-between"
        >
          {value
            ? jurisdictions.find((jurisdiction) => jurisdiction.value === value)?.label
            : "Select jurisdiction..."}
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-[180px] p-0">
        <Command>
          <CommandInput placeholder="Search jurisdiction..." />
          <CommandList>
            <CommandEmpty>No jurisdiction found.</CommandEmpty>
            <CommandGroup>
              {jurisdictions.map((jurisdiction) => (
                <CommandItem
                  key={jurisdiction.value}
                  value={jurisdiction.value}
                  onSelect={() => {
                    onChange(jurisdiction.value);
                    setOpen(false);
                  }}
                >
                  <Check
                    className={cn(
                      "mr-2 h-4 w-4",
                      value === jurisdiction.value ? "opacity-100" : "opacity-0"
                    )}
                  />
                  {jurisdiction.label}
                </CommandItem>
              ))}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  );
} 